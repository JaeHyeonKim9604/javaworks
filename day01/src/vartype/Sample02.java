package vartype;

public class Sample02 {

	public static void main(String[] args) {
		// 문자형 변수 선언 및 초기화
		// 한 문자인 경우에는 따옴표('')
		char ch = 'A';
		
		// 문자열인 경우에는 큰따옴표(""), String 자료형사
		String ch2 = "AB";
		String name; //변수 선언
		name = "김형준"; //변수 초기화
		
		System.out.println(ch);
		System.out.println(ch2);
		System.out.println(name);
		
		/* 변수 이름 주의
		숫자로 시작하면안됨*/

	}

}
