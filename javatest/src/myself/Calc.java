package myself;

public class Calc {
	
	public int SUM(int a, int b) {
		int sum = a + b;
		return sum;
	}
	
	public int MIN(int a, int b) {
		int min = a - b;
		return min;
	}
	
	public int MUL(int a, int b) {
		int mul = a * b;
		return mul;
	}
	
	public int DEV(int a, int b) {
		int dev = a / b;
		return dev;
	}
}
